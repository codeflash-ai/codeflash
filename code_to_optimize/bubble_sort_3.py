def sorter(arr):
    # Insertion sort: much faster than bubble sort for nearly-sorted or small lists
    for i in range(1, len(arr)):
        key = arr[i]
        j = i - 1
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key
    return arr
